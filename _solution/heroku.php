              <h4 class="summary-list-item__ttl--large">■ Herokuを活用するメリット</h4>
              <p class="solution-detail-content__txt">Herokuを活用してSalesforce<sup>®</sup>環境のデータを共有する高機能なカスタムアプリケーションを短期間で開発できます。Salesforce<sup>®</sup>とHerokuのデータベースを双方向に同期することによって、Salesforce<sup>®</sup>データベースの連絡先や取引先、カスタムオブジェクトなどのデータを統合できます。<br>また、オンプレミスやクラウド上の基幹業務システムのデータベースや他のクラウドサービスのデータについても、HerokuのコネクタやAPIで容易にデータ統合できます。<br>Salesforce<sup>®</sup>のデータや既存の業務システムのデータを有効に活用するカスタムアプリケーションを開発することにより、データの利用価値を高めることができます。</p>
              <ul class="summary-list-item__list">
                <li class="summary-list-item__list-item">Sales CloudやService Cloud、Marketing Cloudを補完する拡張性ある独自のカスタムアプリケーションが作れます。</li>
