                <li class="summary-list-item__list-item">Herokuを活用すれば、Salesforce<sup>®</sup>の制約条件（ガバナ制限※）を気にすることなく、Salesforce<sup>®</sup>や業務システムなどと連携する幅広い業務でのカスタムアプリケーションの開発やデータ処理が可能となります。<br>Salesforce<sup>®</sup>では制限より開発が難しいアプリケーションや大量データの処理など、Herokuのコンテナ環境、データベース環境を使えば安全でスケーラブルにシステムを拡張することができます。</li>
              </ul>
