              <h4 class="summary-list-item__ttl--large">■ Salesforceからのカスタムデータの有効活用</h4>
              <p class="solution-detail-content__txt">さらに、Heroku内のカスタムデータベースのデータを Salesforce<sup>®</sup>から扱うことができます（参照、追加、更新、削除）。Herokuのカスタムアプリケーションで登録されたデータや、基幹業務システムやクラウドサービスから取り込んだデータをSalesforce<sup>®</sup>の業務で利用できるため、Herokuのデータベースにあらゆるデータを保管しながら、両者のデータをあたかも1つのように扱うことができます。例えば、Heroku内にアーカイブされた過去データをSalesforce<sup>®</sup>から参照させることも可能となります。<br>これにより、あらゆる業務システム全体で一貫性のあるカスタマーエクスペリエンスを備えた魅力的なアプリケーションを構築することが可能です。</p>
